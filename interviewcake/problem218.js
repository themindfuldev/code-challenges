/*
Write a function to find the 2nd largest element in a binary search tree. 

Here's a sample binary tree node class:

  class BinaryTreeNode {
  constructor(value) {
    this.value = value;
    this.left = null;
    this.right = null;
  }

  insertLeft(value) {
    this.left = new BinaryTreeNode(value);
    return this.left;
  }

  insertRight(value) {
    this.right = new BinaryTreeNode(value);
    return this.right;
  }
}
*/

class BinaryTreeNode {
    constructor(value) {
        this.value = value;
        this.left = null;
        this.right = null;
    }

    insertLeft(value) {
        this.left = new BinaryTreeNode(value);
        return this.left;
    }

    insertRight(value) {
        this.right = new BinaryTreeNode(value);
        return this.right;
    }
}

function findSecondLargest(treeRoot) {
    // If empty or single-node tree, then there is no second element to begin with
    if (!treeRoot || (!treeRoot.left && !treeRoot.right)) {
        throw new Error('tree must have more than 1 element');
    }

    let rightmostNode = treeRoot;
    let rightmostParentNode = null;

    // Find the rightmostNode
    while (rightmostNode.right !== null) {
        rightmostParentNode = rightmostNode;
        rightmostNode = rightmostNode.right;
    }

    // If it has no left children, it's the parent
    if (!rightmostNode.left) {
        return rightmostParentNode.value;
    }

    // Else, search for the right-most children of its left children
    let secondLargestNode = rightmostNode.left;
    while (secondLargestNode.right !== null) {
        secondLargestNode = secondLargestNode.right;
    }
    return secondLargestNode.value;
}

// Tests

let desc = 'full tree';
let treeRoot = new BinaryTreeNode(50);
let leftNode = treeRoot.insertLeft(30);
leftNode.insertLeft(10);
leftNode.insertRight(40);
let rightNode = treeRoot.insertRight(70);
rightNode.insertLeft(60);
rightNode.insertRight(80);
assertEquals(findSecondLargest(treeRoot), 70, desc);

